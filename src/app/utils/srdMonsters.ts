export function findMonster(name: string): Monster | undefined {
  const normalizedSearch = name.toLowerCase().trim();
  return monsters.find(monster => 
    monster.name.toLowerCase() === normalizedSearch
  );
}

export function searchMonsters(query: string): Monster[] {
  const normalizedSearch = query.toLowerCase().trim();
  return monsters.filter(monster => 
    monster.name.toLowerCase().includes(normalizedSearch)
  ).slice(0, 5); // Limit to 5 suggestions
}
